<h2>About the group </h2>
<p>
Looking at small animals with tiny brains, we get the impression that they act effortlessly in the world, foraging for food and returning home safely. In contrast, today's carefully hand-designed computers and robots with all available sensors and processing power are hardly able to successfully perform such simple behaviors. 
</p>
<p>
The world is too complex and too ambiguous to get interpreted reliably with contemporary algorithms. So in which fundamental principles does information processing in brains differ from information processing performed by current computing algorithms? 
</p>
